#include <bits/stdc++.h>
using namespace std;

int main(){
      //四則演算
      cout << 1 + 1 << endl; //加法
      cout << 3 - 4 << endl; //減法
      cout << 2 * 3 << endl; //乗法
      cout << 7 / 3 << endl; //除法
      //剰余演算      
      cout << 7 % 3 << endl;
      cout << 4 % 5 << endl;
      //優先順位
      cout << (1 + 2) * 3 << endl;
}
